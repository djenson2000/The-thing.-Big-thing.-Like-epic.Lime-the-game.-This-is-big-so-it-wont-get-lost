function game_Level_one:update(dt)
    if handleKeyPress("escape") then
        changeState(Menu)
        timer = timer + dt
    end

       -- Move the sprite based on user input
       if love.keyboard.isDown("left") then
        spriteX = spriteX - 100 * dt
    end
    if love.keyboard.isDown("right") then
        spriteX = spriteX + 100 * dt
    end
    if love.keyboard.isDown("up") then
        spriteY = spriteY - 100 * dt
    end
    if love.keyboard.isDown("down") then
        spriteY = spriteY + 100 * dt
    end
    
    -- Check for collisions
    if checkCollisions() then
        -- Handle the collision
        print("Collision detected! at time:", timer)
    end
end



